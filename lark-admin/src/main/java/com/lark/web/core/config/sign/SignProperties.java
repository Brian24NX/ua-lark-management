import java.util.Map;

@Configuration
@ConfigurationProperties(prefix = "sign")
public class SignProperties {
    private Map<String, String> map = new HashMap<>();

    public Map<String, String> getMap() {
        return map;
    }

    public void setMap(Map<String, String> map) {
        this.map = map;
    }
}
